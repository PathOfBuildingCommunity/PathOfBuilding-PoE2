local out = io.open("../Data/Essence.lua", "w")
out:write('-- This file is automatically generated, do not edit!\n')
out:write('-- Item data (c) Grinding Gear Games\n\nreturn {\n')
for essence in dat("Essences"):Rows() do
	if essence.BaseItemType.Id:match("Essence(.+)") then
		out:write('\t["', essence.BaseItemType.Id, '"] = { name = "', essence.BaseItemType.Name, '", ')
		out:write('type = "', essence.BaseItemType.Id:match("Essence(.+)"), '", tierLevel = ', essence.DropLevel[1], ', ')
		out:write('mods = { ')
		for _, essenceMod in ipairs(dat("EssenceMods"):GetRowList("Essence", essence)) do
			for _, itemClass in ipairs(essenceMod.TargetItemCategory.ItemClasses) do
				local mod = essenceMod.Mod1 or essenceMod.Mod2
				if mod ~= nil then
					out:write('["', itemClass.Id, '"] = "', mod.Id, '", ')
				end
			end
		end
		out:write('}, },\n')
	end
end
out:write('}')
out:close()

local out = io.open("../Data/LiquidEmotions.lua", "w")
out:write('-- This file is automatically generated, do not edit!\n')
out:write([[-- Item data (c) Grinding Gear Games

-- spell-checker: disable
return {
]])
for emotion in dat("LiquidEmotionOutcomes"):Rows() do
	local text = string.format('\t["%s"] = { name = "%s", radiusJewel = %s, tierLevel = %s, mods = {',
		emotion.BaseItemType.Id, emotion.BaseItemType.Name, emotion.RadiusJewel == 1, emotion.BaseItemType.DropLevel)
	out:write(text)
	for _, jewelBase in ipairs({ "Ruby", "Sapphire", "Emerald", "Diamond" }) do
		out:write(string.format('["%s"] = {', jewelBase))
		for _, modType in ipairs({ "Prefix", "Suffix" }) do
			local key = jewelBase .. modType
			local mod = emotion[key]
			if mod then
				out:write(string.format('["%s"] = "%s", ', modType, mod.Id))
			end
		end
		out:write("}, ")
	end
	out:write("}, },\n")
end
out:write([[-- spell-checker: enable
}]])
out:close()
print("Essences and emotions exported.")
